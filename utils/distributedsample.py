class ReplayBuffer_TrainingSampler(Sampler):
    """
    In training, we only care about the "infinite stream" of training data.
    So this sampler produces an infinite stream of indices and
    all workers cooperate to correctly shuffle the indices and sample different indices.

    The samplers in each worker effectively produces `indices[worker_id::num_workers]`
    where `indices` is an infinite stream of indices consisting of
    `shuffle(range(size)) + shuffle(range(size)) + ...` (if shuffle is True)
    or `range(size) + range(size) + ...` (if shuffle is False)

    Note that this sampler does not shard based on pytorch DataLoader worker id.
    A sampler passed to pytorch DataLoader is used only with map-style dataset
    and will not be executed inside workers.
    But if this sampler is used in a way that it gets execute inside a dataloader
    worker, then extra work needs to be done to shard its outputs based on worker id.
    This is required so that workers don't produce identical data.
    :class:`ToIterableDataset` implements this logic.
    This note is true for all samplers in detectron2.
    """

    def __init__(self, size: int, shuffle: bool = True, finetune_cycle_num: int = 3, warmup_steps: int = 10, seed: Optional[int] = 0):
        """
        Args:
            size (int): the total number of data of the underlying dataset to sample from
            shuffle (bool): whether to shuffle the indices or not
            seed (int): the initial seed of the shuffle. Must be the same
                across all workers. If None, will use a random seed shared
                among workers (require synchronization among all workers).
        """
        if not isinstance(size, int):
            raise TypeError(f"TrainingSampler(size=) expects an int. Got type {type(size)}.")
        if size <= 0:
            raise ValueError(f"TrainingSampler(size=) expects a positive int. Got {size}.")
        self._size = size
        self._shuffle = shuffle
        self._seed = int(seed)

        self._rank = 0 if not is_dist_avail_and_initialized() else get_rank()
        self._world_size = 1 if not is_dist_avail_and_initialized() else get_world_size()

        self.finetune_cycle_num = finetune_cycle_num
        self.warmup_steps = warmup_steps

    def __iter__(self):
        start = self._rank
        yield from itertools.islice(self._infinite_indices(), start, None, self._world_size)

    def _infinite_indices(self):
        g = torch.Generator()
        g.manual_seed(self._seed)
        while True:
            if self._shuffle:
                indices = torch.randperm(self._size, generator=g).tolist()
                ## insert -1 into indices for indicating to use replay buffer ##
                replay_buffer_indices = []
                for idx, ind in enumerate(indices):
                    replay_buffer_indices.append(ind)
                    if idx >= self.warmup_steps * self._world_size:
                        replay_buffer_indices += [-1 for _ in range(self.finetune_cycle_num)]
                assert len(replay_buffer_indices) == self._size*(1 + self.finetune_cycle_num) - self.warmup_steps*self._world_size*self.finetune_cycle_num
                yield from replay_buffer_indices
            else:
                raise NotImplementedError
                yield from torch.arange(self._size).tolist()
    
    def __len__(self):
        return self._size // self._world_size
